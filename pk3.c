#include <stdio.h>

int main() {
    int p;

    printf("Введіть кількість розрядів p (наприклад: 3):\n");

    // Зчитування значення p
    scanf("%d", &p);

    // Масив для зберігання кількості правильних чисел
    long long dp[31][2]; // [0] — закінчується однією цифрою, [1] — двома однаковими

    // Базовий випадок
    dp[1][0] = 2; // 5, 9
    dp[1][1] = 0; // немає двох однакових поспіль у 1-розрядному числі

    // Динамічне програмування
    for (int i = 2; i <= p; i++) {
        dp[i][0] = dp[i-1][0] + dp[i-1][1]; // додаємо іншу цифру
        dp[i][1] = dp[i-1][0];              // додаємо ще одну таку ж
    }

    long long result = dp[p][0] + dp[p][1];

    printf("Кількість правильних %d-розрядних чисел: %lld\n", p, result);

    return 0;
}
